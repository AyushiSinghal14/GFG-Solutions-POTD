#include <bits/stdc++.h>
using namespace std;

class Array {
  public:
    template <class T>
    static void input(vector<T> &A, int n) {
        for (int i = 0; i < n; i++) {
            scanf("%d ", &A[i]);
        }
    }

    template <class T>
    static void print(vector<T> &A) {
        for (int i = 0; i < A.size(); i++) {
            cout << A[i] << " ";
        }
        cout << endl;
    }
};


// } Driver Code Ends

class Solution {
  public:
    int longestSubseq(int n, vector<int> &a) {
        vector<int> dp(1e3+5,0);
        dp[a[0]]++;
        for(int i=1;i<n;i++)dp[a[i]]=max(dp[a[i]],max(dp[a[i]-1],dp[a[i]+1])+1);
        return *max_element(dp.begin(),dp.end());
    }
};


